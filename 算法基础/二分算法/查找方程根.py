"""
求下面方程的一个根: f(x)= x^3-5x^2+10x-80 = 0
若求出的根是a，则要求|f(a)| <= 10^(-6)
解法: 对f(x)求导， 得f' (x)=3x^2-10x+10。 由一元二次方程求根公式知方程
f' (x)= 0无解，因此f' (x)恒大于0。故f(x)是单调递增的。易知f(0) < 0且
f(100)>0,所以区间[0, 100]内必然有且只有一个根。由于f(x)在[0, 100]内是
单调的，所以可以用二分的办法在区间[0, 100]中寻找根。

"""
EPS = 1e-6
def f(x):
    return x**3 - 5*x*x + 10*x - 80


i = 0
j = 100
root = (i+j)/2
y = f(root)
while abs(y) > EPS:
    if y > 0:
        j = root
    else:
        i = root
    root = (i+j)/2
    y = f(root)

print(f"{root :.8f}")